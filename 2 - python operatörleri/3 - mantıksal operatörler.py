#python dilinde mantıksal operatörler

#burada bir değişken iki değer arasında mı kontrol edebiliriz : ifade<değişken<ifade ile 
#fakat bahsettiğimiz mantıksal ifadeler "and,or,not" ifadeleridir


#---------------------------------------------------------------------------------------------------------------------

x = 5

#and ve or
print((x>5)and(x<10))
print((x>5)or(x<10))

#not operatörü tam tersini alır
print(not((x>5)or(x<10)))